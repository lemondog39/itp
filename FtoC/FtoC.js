// To convert specifically 99 Fahrenheit into Celsius
let f = 99; // defining Fahrenheit (a.k.a f) as 99
const c = ((f - 32)* (5/9)); // function to define c
console.log(c);

// To convert any value of Fahrenheit into Celsius
let f = prompt("What is the temperature in Fahrenheit?");
const c = ((f - 32)* (5/9)); 
console.log(c);
